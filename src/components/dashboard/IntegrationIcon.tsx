export function IntegrationIcon({ name, iconUrl }: IntegrationIconProps) {
  const [imgFailed, setImgFailed] = useState(false);
  const BuiltinIcon = BUILTIN_ICONS[name];

  if (BuiltinIcon) {
    return <BuiltinIcon />;
  }

  if (iconUrl && !imgFailed) {
    return (
      <img
        alt={`${name} logo`}
        className="size-full object-contain"
        loading="lazy"
        src={iconUrl}
        onError={() => setImgFailed(true)}
      />
    );
  }

  return (
    <span className="flex size-full items-center justify-center text-xs font-semibold uppercase text-muted-foreground">
      {name.trim().charAt(0) || <Puzzle className="size-4" strokeWidth={1.5} />}
    </span>
  );
}
